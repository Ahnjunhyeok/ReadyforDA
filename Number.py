# 정수형(양의 정수, 음의 정수, 0)
a = 123
b = -123
c = 0

print(a, b, c)

# 실수형(소수점이 포함된 숫자)
a = 1.2
b = -3.45

print(a, b)

# 컴퓨터식 지수 표현 방식
a = 4.24E10
b = 4.24e-10

print(a, b)

# 8진수
a = 0o177

print(a)

# 16진수
a = 0x8ff
b = 0xABC

print(a, b)

# 사칙연산
a = 3
b = 4

print(a+b)

# x의 y 제곱을 나타내는 ** 연산자
a = 3
b = 4
print(a ** b)

# 나눗셈 후 나머지를 반환하는 % 연산자
print(7 % 3)

# 나눗셈 후 몫을 반환하는 // 연산자
print(10 // 3)

# Quiz : 14를 3으로 나눈 몫과 나머지는?
print(14 % 3)
print(14 // 3)